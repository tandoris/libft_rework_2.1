#include "libft.h"

void	ft_putnbri(int *n, int i)
{
	int a;

	a = 0;
	while (i > a)
	{
		ft_putnbr(n[a]);
		if (i > ++a)
			ft_putstr(", ");
	}
	ft_putchar('\n');
}
