#include "libft.h"

int			ft_atoi(const char *str)
{
	int		ng;
	size_t	res;

	res = 0;
	ng = 1;
	while (*str == '\t' || *str == ' ' || *str == '\n' ||
		*str == '\v' || *str == '\f' || *str == '\r')
		str++;
	if (*str == '-' || *str == '+')
		if (*str++ == '-')
			ng = -ng;
	while (*str > 47 && *str < 58)
	{
		if (ng < 0 && (res > 922337203685477580 ||
			(res == 922337203685477580 && *str > 55)))
			return (0);
		if (res > 922337203685477580 ||
			(res == 922337203685477580 && *str > 55))
			return (-1);
		res *= 10;
		res += *str++ - 48;
	}
	return (res * ng);
}
